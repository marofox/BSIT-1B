fruits = ['mango','guava','atis','pomelo','pakwan']
print(fruits)
print(fruits[3])
print(fruits[3 : 6]) #list slicing


fruits.append("lemon") #it can add variable sa list  
print(fruits)

fruits.insert(3 , "lime")# it can add 
print(fruits)

fruits.remove('mango') #tatanggalin ang variable sa list
print(fruits)

fruits.pop()# aalisin niya yung nasa last 
print(fruits)

fruits.sort()#it can arrange alphabetically
print(fruits)

print(len(fruits))#binibilang ang list

fruits.reversed()# Z-A pabaliktad na alphanetically
print(fruits)

